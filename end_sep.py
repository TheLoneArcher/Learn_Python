a, b, c, d = 1, 2, 3, 4
# sep is for elements in the same print statement
print(a, b, c, d, sep = "\t", end = "\nThis is due to the end argument.\n")
# the end argument is printed at the end of the print statement it is used in
# the default print statement ends with a newline character, so end = "\n" by default
print("The other print statement.")